public class Fragment {
    private final int idFrag;
    private final String nameFrag;

    public Fragment(int idFrag, String nameFrag) {
        this.idFrag = idFrag;
        this.nameFrag = nameFrag;
    }

    public int getIdFrag() {
        return this.idFrag;
    }

    public String getNameFrag() {
        return this.nameFrag;
    }
}
